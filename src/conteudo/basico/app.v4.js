/* Aula 02 - Operações Aritméticas */

// tenta realizar a soma de string com inteiros.
console.log("Ano" + 2020);
console.log("2" + "2");

// usando o método parse, ele realiza a conversão de tipos.
console.log(parseInt("2") + parseInt("2"));

// no caso de uma conversão onde passamos uma string, ele faz automaticamente o parse para inteiro.
console.log("10" / "2");
console.log("Ricardo" / 2); // o retorno é NaN (Not a Number)
console.log(6.5); // para declarar números com vírgula, usamos o ponto.
console.log(6,5); // ao usar vírgula, o JS interpreta como dois valores distintos.
